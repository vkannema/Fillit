#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*dst;

	i = 0;
	if (!s || !f)
		return (NULL);
	if (!(dst = (char *)malloc(sizeof(char) * ft_strlen((char *)s) + 1)))
		return (0);
	while (s[i])
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
